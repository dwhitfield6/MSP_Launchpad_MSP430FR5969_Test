int main (void)
{
    unsigned long i;

    WDTCTL = WDTPW | WDTHOLD;               // Stop watchdog timer

    /* Initialize */
    SYS_ConfigureOscillator();
    Init_App();
    Init_System();

    while(1)
    {
    	LED_GreenToggle();
    	MSC_DelayUS(5000);

    }
	return 0;
}

